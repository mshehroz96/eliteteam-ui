$(document).on("click", "a.proxy_toggle_table_row", function (e) {
  e.preventDefault();
  var strRowGroup = $(this).attr("data-rowgroup");
  $("tr[data-rowgroup='" + strRowGroup + "']").toggleClass("d-none");
  if ($("tr[data-rowgroup='" + strRowGroup + "']").hasClass("d-none")) {
    $(this).find("i.proxy_expand_collapse_icon").removeClass("bx-minus").addClass("bx-plus");
  } else {
    $(this).find("i.proxy_expand_collapse_icon").removeClass("bx-plus").addClass("bx-minus");
  }
});
